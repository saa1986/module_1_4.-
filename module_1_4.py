my_string = input( "Введите фразу: ")
print(len(my_string )) # сумма символов в веденной строке
print(my_string.upper()) # вывод в верхнем регистре
print(my_string.lower()) # вывод в нижнем регистре
print(my_string.replace(" ", "")) # изменяем строку, удаляя все пробелы
print(my_string[0]) # вывод первого символа
print(my_string[-1])# вывод последнего символа